list=[1,8,7,2,21,15]
print(list)
# sort
list.sort()
print(list)

#reverse the list
list.reverse()
print(list)

#append add 0 at the end
list.append(0)
print(list)

# insert 10 at index 2
list.insert(2,10)
print(list)

#delete last element
list.pop()
print(list)

#delete particular element, index no.
list.pop(3)
print(list)

#remove particular element
list.remove(21)
print(list)
#if list has duplicate items first occurance will be removed

